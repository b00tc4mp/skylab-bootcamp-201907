const FavButton = require('./fav-button')

function DuckDetail({ id, favorite, title, imageUrl, price, description, link }) {
    return `<article>
        <h3>${title}</h3>
        <img src="${imageUrl}">
        <span>${price}</span>
        <p>${description}</p>
        <a href="${link}" target="_blank">Go to store</a>
        ${FavButton(id, favorite)}
    </article>`
}

module.exports = DuckDetail